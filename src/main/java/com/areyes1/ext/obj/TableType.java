package com.areyes1.ext.obj;

/**
 * The Class TableType.
 */
public class TableType {

	/** The id. */
	private int id;
	
	/** The type. */
	private String type;
	
	/** The table. */
	private String table;
	
	/** The column. */
	private String column;
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	/**
	 * Gets the table.
	 *
	 * @return the table
	 */
	public String getTable() {
		return table;
	}
	
	/**
	 * Sets the table.
	 *
	 * @param table the new table
	 */
	public void setTable(String table) {
		this.table = table;
	}
	
	/**
	 * Gets the column.
	 *
	 * @return the column
	 */
	public String getColumn() {
		return column;
	}
	
	/**
	 * Sets the column.
	 *
	 * @param column the new column
	 */
	public void setColumn(String column) {
		this.column = column;
	}
	
	
}
